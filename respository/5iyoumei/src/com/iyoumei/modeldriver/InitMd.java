package com.iyoumei.modeldriver;

public class InitMd extends BasicMd {
	private String versionTag;// 版本tag
	private String brandTag;// 品牌tag
	private String cityTag;// 城市tag
	public String getVersionTag() {
		return versionTag;
	}
	public void setVersionTag(String versionTag) {
		this.versionTag = versionTag;
	}
	public String getBrandTag() {
		return brandTag;
	}
	public void setBrandTag(String brandTag) {
		this.brandTag = brandTag;
	}
	public String getCityTag() {
		return cityTag;
	}
	public void setCityTag(String cityTag) {
		this.cityTag = cityTag;
	}

}
